#include <iostream>

using namespace std;

int main()
{
    int n = 0;

    while(n!=10){
        cout << n << endl;
        n++;
    }

    cout << endl;

    do{
        cout << n << endl;
        n++;
    }while(n<=14);

    cout << endl;

    for(int i=0; i<10;i++){
        cout << i << endl;
    }




    return 0;
}
